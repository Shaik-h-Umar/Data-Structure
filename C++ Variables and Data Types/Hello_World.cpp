// Program to print hello world
#include<iostream> // it is basically a directive which is processed by a pre-processor. it is a header file
using namespace std; // it is basically a container for the standard library
int main(){ // this is the main method from where the execution of the code begins.
    cout<<"Hello World";   //cout<< --> this is used to print something
    return 0; // this denotes the end of the program
    cout<<"Hey bro"; // this will not execute because the compiler have already seen the end of the program 
}


/* 
Output:
Hello World
*/